### 模型的选择
estModel () {
	EMfa=$1
	EMprog=$2
	#https://links.jianshu.com/go?to=https%3A%2F%2Fgithub.com%2Fddarriba%2Fjmodeltest2%2Freleases%2Ftag%2Fv2.1.10r20160303
	if [[ "$EMprog" == "jModelTest" ]]; then  #88种模型
		java -jar jModelTest.jar -d $EMfa -f -i -g 4 -s 11 -BIC -AIC -v -a -tr 40 -o 输出结果.txt
	#https://links.jianshu.com/go?to=https%3A%2F%2Fgithub.com%2Fddarriba%2Fprottest3%2Freleases%2Ftag%2F3.4.2-release
