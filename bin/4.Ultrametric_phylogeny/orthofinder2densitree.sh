### seqRenamer
### require FuhaoBin::fasta.renamer.by.dict.pl
### produce tmp file
seqRenamer () {
	SRinfa=$1
	SRdict=$2
	SRoutfa=$3
#	SRfile_str=$(date '+%Y%m%d%H%M%S%N')
	SRfile_str=$(date '+%Y%m%d%H%M%S')
	SRtmpfile="000tmp.$SRfile_str.idlist"
	grep ^'>' "$SRinfa" | sed 's/^>//;s/\s\+.*$//;' > $SRtmpfile
	if [ ! -s "$SRtmpfile" ]; then
		echo "Error: empty rename ID list" >&2
		return 100
	fi
	grep -w -f $SRtmpfile $SRdict > "$SRoutfa.dict"
	if [ ! -s "$SRoutfa.dict" ]; then
		echo "Error: empty dict ID list" >&2
		echo "    CMD: grep -w -f $SRtmpfile $SRdict > $SRoutfa.dict" >&2
		return 100
	fi
	rm $SRtmpfile
	fasta.renamer.by.dict.pl "$SRinfa" "$SRoutfa.dict" "$SRoutfa"
	if [ $? -ne 0 ] || [ ! -s "$SRoutfa" ]; then
		echo "Error: failed to rename: $SRinfa" >&2
		echo "    CMD: fasta.renamer.by.dict.pl $SRinfa $SRoutfa.dict$SRoutfa" >&2
		return 100
	fi
	return 0
}
### produce no tmp file
seqRenamer2 () {
	SRinfa=$1
	SRdict=$2
	SRoutfa=$3
	SRstr=""
	SRstr=$(grep ^'>' "$SRinfa" | perl -lne 'BEGIN{@arr=();} s/^>//;s/\s+.*$//;push (@arr,$_); END {print join("|", @arr);}')
	if [ -z "$SRstr" ]; then
		echo "Error: empty rename ID list: $SRinfa" >&2
		return 100
	fi
	grep -w -E "$SRstr" $SRdict > "$SRoutfa.dict"
	if [ ! -s "$SRoutfa.dict" ]; then
		echo "Error: empty dict ID list" >&2
		return 100
	fi
	fasta.renamer.by.dict.pl "$SRinfa" "$SRoutfa.dict" "$SRoutfa"
	if [ $? -ne 0 ] || [ ! -s "$SRoutfa" ]; then
		echo "Error: failed to rename: $SRinfa" >&2
		echo "    CMD: fasta.renamer.by.dict.pl $SRinfa $SRoutfa.dict $SRoutfa" >&2
		return 100
	fi
	return 0
}



